// Função - console.log();

console.log('Hello "World"'); // tudo que estiver dentro de: 'aspas simples'
console.log("Hello 'World'"); // ou dentro de "aspas duplas"
console.log(`'Hello' "World"`); // ou dentro de `crases`, é considerado String (texto) em linguagem de programação

console.log(123456); // número interiro - Number = Número
console.log(1234.567); // número com ponto flutuante
console.log(36, 1.98, 'Lucivaldo Junior') // separados pór vírgula

/* Sempre que precisar debugar o código ou exibi na tela a função é: console.log()
Só é exibida no terminal ou no console do navegador através do Inspecionar, na aba console.*/