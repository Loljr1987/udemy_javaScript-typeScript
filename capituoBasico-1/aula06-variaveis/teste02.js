let nome; // Variável declarada
nome = "Lucivaldo"; // Inicializando a variável
console.log(nome);
nome = 'Jaisielle'; // Nesse momento a variável tem seu valor mudado.
console.log(nome);


/* 
** Variáveis não podem criadas com palavras reservados a linguagens;
** Precisam ter nopmes significativos, que façam, sentido ao que se propõee;
** Não podem iniciar com números, acentos, espaçoes em branco ou traços;
** São case-sensitive, ou seja, letras maiúsculas ou minúsculas fazem toda diferença;
** É recomendado utilizar camelCase;
** Não se pode declarar novamente uma variável 'let'.
*/